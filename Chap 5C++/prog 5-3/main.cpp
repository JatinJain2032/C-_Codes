//This prog uses a while loop to display hello five times
#include <iostream>

using namespace std;

int main()
{
   int number=0;  //This is intialization exp

   while(number<5) //this is tets exp
   {
       cout<<"Hello"<<endl;
       number++;  //This is update exp , prog will continue until number is equal to 5
   }
   cout<<"That's all"<<endl;
    return 0;
}
